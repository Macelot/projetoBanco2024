    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        Connection c;
        c = conecta();
        try {         
            // Desabilita o autocommit para iniciar a transação manualmente
            c.setAutoCommit(false);
    
            String comando = "INSERT INTO users ("
                    + "username,"
                    + "password,"
                    + "quant) "
                    + "VALUES ("
                    + "?,"
                    + "?,"
                    + "?)";
            PreparedStatement stmt;
            stmt = c.prepareStatement(comando);
            stmt.setString(1, "user01");
            stmt.setString(2, "123456");
            stmt.setInt(3, 0);
            stmt.execute();
            
            stmt = c.prepareStatement(comando);
            stmt.setString(1, "neo");
            stmt.setString(2, "abc");
            stmt.setInt(3, 0);
            stmt.execute();
            
            stmt = c.prepareStatement(comando);
            stmt.setString(1, "morpheu");
            stmt.setString(2, "xxx");
            stmt.setInt(3, 3);
            stmt.setInt(4, 11111);
            stmt.execute();
            
            // Confirma a transação
            c.commit();
            
        } catch (Exception e) {
            System.out.println("Erro "+e.getMessage());
            try {
                if (c != null) {
                    c.rollback();
                }
            } catch (SQLException se) {
                System.out.println("Erro no rollback: " + se.getMessage());
            }
        }finally {
            try {
                // Restaura o autocommit para o comportamento padrão
                if (c != null) {
                    c.setAutoCommit(true);
                }
            } catch (SQLException se) {
                System.out.println("Erro ao restaurar autocommit: " + se.getMessage());
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed


