    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        clicou = jTable1.getValueAt(
                jTable1.getSelectedRow(), 
                0);
        System.out.println("Clicou "+clicou);
        String sql = "SELECT * FROM veiculos WHERE id=?";
        Connection c = conecta();
        try {
            PreparedStatement stmt = c.prepareStatement(sql);
            int id = Integer.valueOf((String) clicou);
            stmt.setInt(1, id);
            ResultSet result = stmt.executeQuery();
            result.next();
            String modelo = result.getString("modelo");
            txtModelo.setText(modelo);
            
            Integer marca = result.getInt("marca_id");
            txtMarca.setText(String.valueOf(marca));
           
            Integer quilometragem = result.getInt("quilometragem");
            txtQuilometragem.setText(String.valueOf(quilometragem));
            
            Integer ano = result.getInt("ano_fabricacao");
            txtAno.setText(String.valueOf(ano));
            
            Double valor = result.getDouble("valor");
            txtValor.setText(String.valueOf(valor));
            
        } catch (Exception e) {
            System.out.println("Erro "+e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlteracoesActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE veiculos set "
                + "modelo=?,"
                + "ano_fabricacao=?,"
                + "quilometragem=?,"
                + "valor=?,"
                + "marca_id=? "
                + "WHERE id=?";
        try {
            Connection c = conecta();
            PreparedStatement stmt = c.prepareStatement(sql);
            String modelo = txtModelo.getText();
            stmt.setString(1, modelo);
            
            Integer ano =Integer.valueOf(txtAno.getText());
            stmt.setInt(2, ano);
            
            Integer quilometragem =Integer.valueOf(txtQuilometragem.getText());
            stmt.setInt(3, quilometragem);
            
            Double valor = Double.valueOf(txtValor.getText());
            stmt.setDouble(4, valor);
            
            Integer marca =Integer.valueOf(txtMarca.getText());
            stmt.setInt(5, marca);
            
            int idClicado = Integer.parseInt((String)clicou);
            stmt.setInt(6, idClicado);
            stmt.executeUpdate();
            btnMontaTabela.doClick();
            
        } catch (Exception e) {
            System.out.println("Erro "+e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarAlteracoesActionPerformed


