    private void btnMontaTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMontaTabelaActionPerformed
        // TODO add your handling code here:
        int total = Integer.parseInt(jTextField1.getText());
        Object[] col = new Object[3];
        col[0]="Id";
        col[1]="Modelo";
        col[2]="Ano";
        Object[][] lin = new Object[total][3];
        String sql = "SELECT * FROM veiculos";
        Connection c = TelaInsert.conecta();
        int cont=0;
        try {
            Statement stmt = c.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while(rs.next()){
                lin[cont][0]=rs.getString("id");
                lin[cont][1]=rs.getString("modelo");
                lin[cont][2]=rs.getInt("ano_fabricacao");
                cont++;
            }
        } catch (SQLException e) {
            System.out.println("Erro "+e.getMessage());
        }
        DefaultTableModel t;
        t = new DefaultTableModel(lin, col);
        jTable1.setModel(t);
    }//GEN-LAST:event_btnMontaTabelaActionPerformed

